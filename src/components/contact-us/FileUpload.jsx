'use client';
import { useCallback, useState } from 'react';
import PropTypes from 'prop-types';

import Box from '@mui/material/Box';
import Typography from '@mui/material/Typography';
import { useTheme } from '@mui/material/styles';

import SvgIcon from '@/components/SvgIcon';
import useTranslation from '@/hooks/useTranslation';

export default function FileUpload({ onFileSelect }) {
  const theme = useTheme();
  const { t } = useTranslation();
  const [isDragging, setIsDragging] = useState(false);
  const [fileName, setFileName] = useState('');

  const handleDragEnter = (e) => {
    e.preventDefault();
    e.stopPropagation();
    setIsDragging(true);
  };

  const handleDragLeave = (e) => {
    e.preventDefault();
    e.stopPropagation();
    setIsDragging(false);
  };

  const handleDragOver = (e) => {
    e.preventDefault();
    e.stopPropagation();
  };

  const handleDrop = useCallback(
    (e) => {
      e.preventDefault();
      e.stopPropagation();
      setIsDragging(false);

      const files = e.dataTransfer.files;
      if (files && files.length > 0) {
        onFileSelect(files[0]);
        setFileName(files[0].name);
      }
    },
    [onFileSelect]
  );

  const handleFileChange = (e) => {
    const files = e.target.files;
    if (files && files.length > 0) {
      onFileSelect(files[0]);
      setFileName(files[0].name);
    }
  };

  return (
    <Box
      onDrop={handleDrop}
      onDragOver={handleDragOver}
      onDragEnter={handleDragEnter}
      onDragLeave={handleDragLeave}
      onClick={() => document.getElementById('file-upload-input').click()}
      sx={{
        border: '2px dashed',
        borderColor: isDragging ? theme.palette.primary.main : theme.palette.divider,
        borderRadius: '16px',
        p: 4,
        textAlign: 'center',
        cursor: 'pointer',
        transition: 'border-color 0.3s',
        '&:hover': {
          borderColor: theme.palette.primary.main,
        },
      }}
    >
      <input id="file-upload-input" type="file" hidden onChange={handleFileChange} />
      <SvgIcon name="tabler-upload" size={48} sx={{ mb: 1, color: 'text.secondary' }} />
      <Typography variant="h6" sx={{ color: 'text.primary' }}>
        {fileName || t('forms.attachmentDrop')}
      </Typography>
      <Typography variant="body2" sx={{ color: 'text.secondary' }}>
        {t('forms.attachmentSize')}
      </Typography>
    </Box>
  );
}

FileUpload.propTypes = {
  onFileSelect: PropTypes.func.isRequired,
};
